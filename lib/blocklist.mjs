// ASCII 关键词：按「词边界」匹配（分隔符 -_/. 空格视作边界），避免误伤 java/average 等。
const ASCII = [
  "av", "porn", "porno", "xxx", "sex", "nude", "hentai", "escort",
  "casino", "betting", "gambling", "cocaine", "heroin", "meth",
];

/**
 * 找出命中的第一个黑名单词；没有则返回 null。
 * @param {string} text  slug / query / 生成文本
 * @returns {string|null}
 */
export function findBlocked(text){
  const raw = String(text || "").toLowerCase();
  if (!raw) return null;
  // 去分隔符版，抓「色 情」「a-v」这类拆分规避
  const nosep = raw.replace(/[\s\-_/.]+/g, "");
  for (const w of CJK){
    if (raw.includes(w) || nosep.includes(w)) return w;
  }
  // ASCII 走词边界：把分隔符归一成空格后按 token 匹配
  const tokens = raw.replace(/[\s\-_/.]+/g, " ");
  for (const w of ASCII){
    if (new RegExp("(^| )" + w + "( |$)").test(tokens)) return w;
  }
  return null;
}
